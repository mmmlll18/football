 Все матчи на заданном стадионе (пример stadium_id = 2)
```
SELECT
    m.match_id,
    m.date,
    m.status,
    m.home_score,
    m.guest_score
FROM match AS m
WHERE m.stadium_id = 2
ORDER BY m.date;
```
